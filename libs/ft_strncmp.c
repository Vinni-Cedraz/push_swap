#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		difference;

	i = 0;
	difference = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < n)
		i++;
	if (i == n)
		return (difference);
	difference = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (difference);
}

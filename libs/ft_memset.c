#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	chr;

	str = (unsigned char *)s;
	chr = (unsigned char)c;
	if (!str)
		return (NULL);
	while (n--)
		*(str + n) = chr;
	return (s);
}
